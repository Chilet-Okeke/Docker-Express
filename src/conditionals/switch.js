// Switch statements
// using switch, case and break 
// using default
// create a loop to generate different dice numbers every 5 seconds

const dice = 2

switch (dice) {
    case 1:
        console.log("You rolled a 1")
        break;
    case 2:
        console.log("You rolled a 2");
        break;
    case 3:
        console.log("You rolled a 3");
        break;
    case 4:
        console.log("You rolled a 4");
        break;
    case 5:
        console.log("You rolled a 5");
        break;
    case 6:
        console.log("You rolled a 6");
        break;
    default:
        console.log("Invalid roll");  
}

